CREATE OR REPLACE PROCEDURE DBA.SP_PROCESSA_CUSTO_MEDIO_COMPRA_CMC()
LANGUAGE SQL
MODIFIES SQL DATA
BEGIN

    DECLARE LS_COMMIT CHAR(6) DEFAULT 'COMMIT';
    DECLARE LD_CMCCALCULADO, LD_QTDESTCPCALCULADO DECIMAL(15,6);
    DECLARE LS_MEMORIACALC VARCHAR(600);
    DECLARE LI_SESSIONID INTEGER;

    SET LI_SESSIONID = ( SELECT
                            AGENT_ID
                        FROM
                            SYSIBMADM.APPLICATIONS
                        WHERE
                            APPL_ID = DBA.UF_APPLICATION_ID());

    INSERT INTO DBA.CUSTO_MEDIO_COMPRA_CTRLTRIGGER (IDSESSIONPROC) VALUES (LI_SESSIONID);

    -- Loop da fila
    FOR ROW_FILA AS CURSOR_FILA CURSOR WITH HOLD FOR
        SELECT
            IDMOVIMENTO     AS I_IDMOV,
            IDEMPRESA       AS I_EMP,
            IDPLANILHA      AS I_PLA,
            IDPRODUTO       AS I_PRO,
            IDSUBPRODUTO    AS I_SUB,
            NUMSEQUENCIA    AS I_SEQ,
            DTMOVIMENTO     AS I_DTM,
            CMCATUAL        AS I_CMC
        FROM
            DBA.CUSTO_MEDIO_COMPRA_PROCESSAMENTO
        WHERE
            STATUS = 'P'
        ORDER BY
            DTINCLUSAO
    DO

        -- Loop dos movimentos posteriores
        FOR ROW_MOV AS CURSOR_MOV CURSOR WITH HOLD FOR
            WITH EMPRESAS_AGRUPAMENTO AS (
                SELECT DISTINCT
                    ACE_GRUPO.IDEMPRESA
                FROM
                    DBA.AGRUPAMENTO_CUSTO_EMPRESA ACE_PARAM
                JOIN
                    DBA.AGRUPAMENTO_CUSTO AC ON
                        AC.IDAGRUPAMENTOCUSTO = ACE_PARAM.IDAGRUPAMENTOCUSTO
                JOIN
                    DBA.AGRUPAMENTO_CUSTO_TIPO ACT ON
                        ACT.IDAGRUPAMENTOTIPO = AC.TIPOAGRUPAMENTO
                JOIN
                    DBA.AGRUPAMENTO_CUSTO_EMPRESA ACE_GRUPO ON
                        ACE_GRUPO.IDAGRUPAMENTOCUSTO = ACE_PARAM.IDAGRUPAMENTOCUSTO
                WHERE
                    ACE_PARAM.IDEMPRESA = ROW_FILA.I_EMP AND
                    ACT.SIGLA = 'CMC'
                UNION ALL
                SELECT
                    ROW_FILA.I_EMP
                FROM
                    SYSIBM.SYSDUMMY1
                WHERE
                    NOT EXISTS (
                        SELECT
                            1
                        FROM
                            DBA.AGRUPAMENTO_CUSTO_EMPRESA ACE_PARAM
                        JOIN
                            DBA.AGRUPAMENTO_CUSTO AC ON
                                AC.IDAGRUPAMENTOCUSTO = ACE_PARAM.IDAGRUPAMENTOCUSTO
                        JOIN
                            DBA.AGRUPAMENTO_CUSTO_TIPO ACT ON
                                ACT.IDAGRUPAMENTOTIPO = AC.TIPOAGRUPAMENTO
                        JOIN
                            DBA.AGRUPAMENTO_CUSTO_EMPRESA ACE_GRUPO ON
                                ACE_GRUPO.IDAGRUPAMENTOCUSTO = ACE_PARAM.IDAGRUPAMENTOCUSTO
                        WHERE
                            ACE_PARAM.IDEMPRESA = ROW_FILA.I_EMP AND
                            ACT.SIGLA = 'CMC'
                    )
            )
            SELECT
                MC.IDEMPRESA             AS I_IDEMPRESA,
                MC.IDPLANILHA            AS I_IDPLANILHA,
                MC.IDPRODUTO             AS I_IDPRODUTO,
                MC.IDSUBPRODUTO          AS I_IDSUBPRODUTO,
                MC.NUMSEQUENCIA          AS I_NUMSEQUENCIA,
                MC.DTMOVIMENTO           AS I_DTMOVIMENTO,
                MC.VALCUSTOMEDIOCOMPRA   AS I_CMCOLD,
                MC.QTDESTOQUECOMPRA      AS I_QTDOLD
            FROM
                DBA.MOVIMENTO_CUSTO MC
            JOIN
                EMPRESAS_AGRUPAMENTO EA ON
                    EA.IDEMPRESA = MC.IDEMPRESA
            WHERE
                MC.IDPRODUTO        = ROW_FILA.I_PRO AND
                MC.IDSUBPRODUTO     = ROW_FILA.I_SUB AND
                MC.DTMOVIMENTO      >= ROW_FILA.I_DTM AND
                MC.FLAGALTEROUCUSTO = 'T'  AND
                (MC.ORIGEMMOVIMENTO  IN ('NFE', 'PRD') OR
                (MC.ORIGEMMOVIMENTO IN ('SIP') AND
                EXISTS (
                    SELECT
                        0
                    FROM
                        DBA.DESAGREGACAO_MOVIMENTO_NOTAS
                    WHERE
                        IDEMPRESA = MC.IDEMPRESA AND
                        IDPLANILHA = MC.IDPLANILHA
                    )
                ))
            ORDER BY
                MC.DTMOVIMENTO,
                MC.NUMSEQUENCIA
        DO
            SET (LD_CMCCALCULADO, LD_QTDESTCPCALCULADO, LS_MEMORIACALC) = (SELECT CMCCALCULADO, QTDESTCPCALCULADO, MEMORIA FROM TABLE (DBA.UF_CALCULADORA_CMC ( ROW_MOV.I_IDEMPRESA, ROW_MOV.I_IDPLANILHA, ROW_MOV.I_IDPRODUTO, ROW_MOV.I_IDSUBPRODUTO, ROW_MOV.I_NUMSEQUENCIA, ROW_MOV.I_DTMOVIMENTO)));

            UPDATE
                DBA.MOVIMENTO_CUSTO
            SET
                VALCUSTOMEDIOCOMPRA = COALESCE(LD_CMCCALCULADO,0),
                QTDESTOQUECOMPRA    = COALESCE(LD_QTDESTCPCALCULADO,0)
            WHERE
                IDEMPRESA    = ROW_MOV.I_IDEMPRESA AND
                IDPLANILHA   = ROW_MOV.I_IDPLANILHA AND
                IDPRODUTO    = ROW_MOV.I_IDPRODUTO AND
                IDSUBPRODUTO = ROW_MOV.I_IDSUBPRODUTO AND
                NUMSEQUENCIA = ROW_MOV.I_NUMSEQUENCIA;

            UPDATE
                DBA.MOVIMENTO_CUSTO
            SET
                VALCUSTOMEDIOCOMPRA = COALESCE(LD_CMCCALCULADO,0),
                QTDESTOQUECOMPRA    = COALESCE(LD_QTDESTCPCALCULADO,0)
            WHERE
                ORIGEMMOVIMENTO = 'MCC' AND
                IDEMPRESA    = ROW_MOV.I_IDEMPRESA AND
                IDPLANILHA   = ROW_MOV.I_IDPLANILHA AND
                NUMSEQUENCIA = ROW_MOV.I_NUMSEQUENCIA;

            /*TRC*/
            UPDATE
                DBA.MOVIMENTO_CUSTO MC
            SET
                VALCUSTOMEDIOCOMPRA = COALESCE(LD_CMCCALCULADO,0),
                QTDESTOQUECOMPRA    = COALESCE(LD_QTDESTCPCALCULADO,0)
            WHERE
                ORIGEMMOVIMENTO = 'TRC' AND
                IDPRODUTO    = ROW_MOV.I_IDPRODUTO AND
                IDSUBPRODUTO = ROW_MOV.I_IDSUBPRODUTO AND
                NUMSEQUENCIA = ROW_MOV.I_NUMSEQUENCIA AND
                EXISTS (
                            SELECT
                                                0
                                        FROM
                                                DBA.NOTAS NF
                                        WHERE
                                                NF.NUMNOTA = ROW_MOV.I_IDPLANILHA AND
                                                NF.IDPLANILHA = MC.IDPLANILHA
                                        );
            /*TRC*/

            INSERT INTO DBA.CUSTO_MEDIO_COMPRA_PROCHIST (IDEMPRESA, IDPLANILHA, IDPRODUTO, IDSUBPRODUTO, NUMSEQUENCIA, DTMOVIMENTO, CMCOLD, CMCNEW, QTDOLD, QTDNEW, DTALTERACAO, IDPLANILHAORI)
            VALUES (ROW_MOV.I_IDEMPRESA, ROW_MOV.I_IDPLANILHA, ROW_MOV.I_IDPRODUTO, ROW_MOV.I_IDSUBPRODUTO, ROW_MOV.I_NUMSEQUENCIA, ROW_MOV.I_DTMOVIMENTO, ROW_MOV.I_CMCOLD, LD_CMCCALCULADO, ROW_MOV.I_QTDOLD, LD_QTDESTCPCALCULADO, SYSDATE, ROW_FILA.I_PLA);

            EXECUTE IMMEDIATE LS_COMMIT;

        END FOR;

        UPDATE
            DBA.CUSTO_MEDIO_COMPRA_PROCESSAMENTO
        SET
            STATUS = 'C'
        WHERE
            IDMOVIMENTO = I_IDMOV;

        EXECUTE IMMEDIATE LS_COMMIT;

    END FOR;

    DELETE FROM DBA.CUSTO_MEDIO_COMPRA_CTRLTRIGGER WHERE IDSESSIONPROC = LI_SESSIONID;

END
