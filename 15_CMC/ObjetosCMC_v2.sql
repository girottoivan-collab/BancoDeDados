    SET LI_COUNT = (SELECT COUNT(0) FROM DBA.MOVIMENTO_CUSTO MC WHERE MC.IDEMPRESA = O.IDEMPRESA AND MC.IDPRODUTO = O.IDPRODUTO AND MC.IDSUBPRODUTO = O.IDSUBPRODUTO AND MC.DTMOVIMENTO > O.DTMOVIMENTO);

