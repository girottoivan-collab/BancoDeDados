CREATE OR REPLACE FUNCTION DBA.VALIDA_CTR_CMC
(
    P_IDEMPRESA INTEGER
)
RETURNS CHAR(1)
LANGUAGE SQL
READS SQL DATA
DETERMINISTIC
BEGIN ATOMIC

    IF EXISTS (
        SELECT 1
        FROM DBA.CONFIG_ATRIBUTO_VALORES CAV
        WHERE CAV.IDATRIBUTO = 490
          AND CAV.IDEMPRESA = P_IDEMPRESA
          AND CAV.V_VARCHAR = 'T'
    ) THEN
        RETURN 'T';
    END IF;

    IF EXISTS (
        SELECT 1
        FROM DBA.CONFIG_ENTIDADE CE
        WHERE CE.ENTIDADE = 'CTR'
          AND CE.CHAVE1 = 0
          AND CE.CHAVE2 = 9
          AND CE.DADOS = 'T'
    ) THEN
        RETURN 'T';
    END IF;

    RETURN 'F';

END
GO
CREATE OR REPLACE TRIGGER DBA.TR_ATCMC_FILA_AI BEFORE INSERT ON DBA.MOVIMENTO_CUSTO
REFERENCING NEW AS N
FOR EACH ROW
WHEN ((SELECT DBA.VALIDA_CTR_CMC(N.IDEMPRESA) FROM DBA.DUMMY) = 'T' )
BEGIN

    DECLARE LI_COUNT INTEGER;
    DECLARE LI_SESSIONID INTEGER;

    SET LI_SESSIONID = DBA.UF_GET_ID_CONNECTION();
    SET LI_COUNT = (SELECT COUNT(0) FROM DBA.MOVIMENTO_CUSTO MC WHERE MC.IDEMPRESA = N.IDEMPRESA AND MC.IDPRODUTO = N.IDPRODUTO AND MC.IDSUBPRODUTO = N.IDSUBPRODUTO AND MC.DTMOVIMENTO > N.DTMOVIMENTO);

    IF N.ORIGEMMOVIMENTO IN ('NFE', 'PRD', 'TRC') OR (N.ORIGEMMOVIMENTO IN ('SIP') AND COALESCE((SELECT COUNT(0) FROM DESAGREGACAO_MOVIMENTO_NOTAS WHERE IDEMPRESA = N.IDEMPRESA AND IDPLANILHA = N.IDPLANILHA),0) > 0) THEN
        SET N.VALCUSTOMEDIOCOMPRA = 0.00, QTDESTOQUECOMPRA = 0.00;
        END IF;

    IF /*LI_COUNT > 0 AND*/ (SELECT COUNT(0) FROM DBA.CUSTO_MEDIO_COMPRA_CTRLTRIGGER WHERE IDSESSIONPROC = LI_SESSIONID) = 0 AND N.ORIGEMMOVIMENTO NOT IN ('TRC', 'MCC') THEN
        INSERT INTO DBA.CUSTO_MEDIO_COMPRA_PROCESSAMENTO (IDEMPRESA, IDPLANILHA, IDPRODUTO, IDSUBPRODUTO, NUMSEQUENCIA, DTMOVIMENTO, CMCATUAL, STATUS)
        VALUES (N.IDEMPRESA, N.IDPLANILHA, N.IDPRODUTO, N.IDSUBPRODUTO, N.NUMSEQUENCIA, N.DTMOVIMENTO-2 MINUTES, N.VALCUSTOMEDIOCOMPRA, 'P'            );

    END IF;

END
GO
CREATE OR REPLACE TRIGGER DBA.TR_ATCMC_FILA_AU AFTER UPDATE ON DBA.MOVIMENTO_CUSTO
REFERENCING NEW AS N OLD AS O
FOR EACH ROW
WHEN (N.ORIGEMMOVIMENTO NOT IN ('TRC') AND (SELECT DBA.VALIDA_CTR_CMC(N.IDEMPRESA) FROM DBA.DUMMY) = 'T')
BEGIN

    DECLARE LI_COUNT INTEGER;
    DECLARE LD_DTCTRL TIMESTAMP;
    DECLARE LI_SESSIONID INTEGER;

    SET LI_SESSIONID = DBA.UF_GET_ID_CONNECTION();

    IF N.DTMOVIMENTO > O.DTMOVIMENTO THEN
        SET LD_DTCTRL = O.DTMOVIMENTO-2 MINUTES;
    ELSE
        SET LD_DTCTRL = N.DTMOVIMENTO-2 MINUTES;
    END IF;

    SET LI_COUNT = (SELECT COUNT(0) FROM DBA.MOVIMENTO_CUSTO MC WHERE MC.IDEMPRESA = N.IDEMPRESA AND MC.IDPRODUTO = N.IDPRODUTO AND MC.IDSUBPRODUTO = N.IDSUBPRODUTO AND MC.DTMOVIMENTO > N.DTMOVIMENTO);

    IF O.VALCUSTOMEDIOCOMPRA > 0 AND /*LI_COUNT > 0 AND*/ (SELECT COUNT(0) FROM DBA.CUSTO_MEDIO_COMPRA_CTRLTRIGGER WHERE IDSESSIONPROC = LI_SESSIONID) = 0 THEN
        INSERT INTO DBA.CUSTO_MEDIO_COMPRA_PROCESSAMENTO (IDEMPRESA, IDPLANILHA, IDPRODUTO, IDSUBPRODUTO, NUMSEQUENCIA, DTMOVIMENTO, CMCATUAL, STATUS)
        VALUES (N.IDEMPRESA, N.IDPLANILHA, N.IDPRODUTO, N.IDSUBPRODUTO, N.NUMSEQUENCIA, LD_DTCTRL, N.VALCUSTOMEDIOCOMPRA, 'P'            );
    END IF;

END
GO
