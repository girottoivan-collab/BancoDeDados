    END IF;

    IF LI_COUNT = 0 AND (SELECT COUNT(0) FROM DBA.CUSTO_MEDIO_COMPRA_PROCESSAMENTO WHERE IDEMPRESA = O.IDEMPRESA AND IDPLANILHA = O.IDPLANILHA AND IDPRODUTO = O.IDPRODUTO AND IDSUBPRODUTO = O.IDSUBPRODUTO AND NUMSEQUENCIA = O.NUMSEQUENCIA AND STATUS = 'P') > 0 THEN
