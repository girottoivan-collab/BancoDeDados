CREATE OR REPLACE FUNCTION DBA.UF_SALDOEST_DTPOSICAO_CMC(
   AI_IDPRODUTO INTEGER,
   AI_IDSUBPRODUTO INTEGER,
   AI_DTPOSICAO DATE,
   AI_IDEMPRESA INTEGER
)
RETURNS TABLE (
    IDPRODUTO INTEGER,
    IDSUBPRODUTO INTEGER,
    QTDATUALESTOQUE NUMERIC(15,6)
)
LANGUAGE SQL
RETURN
   WITH EMPRESAS_AGRUPAMENTO AS (
      SELECT
         ACE_GRUPO.IDEMPRESA
      FROM
         DBA.AGRUPAMENTO_CUSTO_EMPRESA ACE_PARAM
      JOIN
         DBA.AGRUPAMENTO_CUSTO AC ON
            AC.IDAGRUPAMENTOCUSTO = ACE_PARAM.IDAGRUPAMENTOCUSTO
      JOIN
         DBA.AGRUPAMENTO_CUSTO_TIPO ACT ON
            ACT.IDAGRUPAMENTOTIPO = AC.TIPOAGRUPAMENTO
      JOIN
         DBA.AGRUPAMENTO_CUSTO_EMPRESA ACE_GRUPO ON
            ACE_GRUPO.IDAGRUPAMENTOCUSTO = ACE_PARAM.IDAGRUPAMENTOCUSTO
      WHERE
         ACE_PARAM.IDEMPRESA = AI_IDEMPRESA AND
         ACT.SIGLA = 'CMC'
      UNION ALL
      SELECT
         AI_IDEMPRESA
      FROM
         SYSIBM.SYSDUMMY1
      WHERE
         NOT EXISTS (
            SELECT
               1
            FROM
               DBA.AGRUPAMENTO_CUSTO_EMPRESA ACE_PARAM
            JOIN
               DBA.AGRUPAMENTO_CUSTO AC ON
                  AC.IDAGRUPAMENTOCUSTO = ACE_PARAM.IDAGRUPAMENTOCUSTO
            JOIN
               DBA.AGRUPAMENTO_CUSTO_TIPO ACT ON
                  ACT.IDAGRUPAMENTOTIPO = AC.TIPOAGRUPAMENTO
            JOIN
               DBA.AGRUPAMENTO_CUSTO_EMPRESA ACE_GRUPO ON
                  ACE_GRUPO.IDAGRUPAMENTOCUSTO = ACE_PARAM.IDAGRUPAMENTOCUSTO
            WHERE
               ACE_PARAM.IDEMPRESA = AI_IDEMPRESA AND
               ACT.SIGLA = 'CMC'
         )
   )
   SELECT
      IDPRODUTO,
      IDSUBPRODUTO,
      SUM(QTDATUALESTOQUE) AS QTDATUALESTOQUE
   FROM (
         SELECT
            ES.IDPRODUTO,
            ES.IDSUBPRODUTO,
            ES.IDEMPRESA,
            ES.IDLOCALESTOQUE,
            ES.QTDATUALESTOQUE,
            ES.DTMOVIMENTO,
            ROW_NUMBER() OVER (PARTITION BY ES.IDPRODUTO, ES.IDSUBPRODUTO, ES.IDLOCALESTOQUE, ES.IDEMPRESA ORDER BY ES.DTMOVIMENTO DESC) LINHA
         FROM
            DBA.ESTOQUE_SINTETICO ES
         JOIN
            EMPRESAS_AGRUPAMENTO EA ON
               EA.IDEMPRESA = ES.IDEMPRESA
         JOIN
            DBA.ESTOQUE_CADASTRO_LOCAL ECL ON
               ECL.IDLOCALESTOQUE = ES.IDLOCALESTOQUE AND
               ECL.FLAGDISPONVENDA = 'T'
         WHERE
            ES.IDPRODUTO = AI_IDPRODUTO AND
            ES.IDSUBPRODUTO = AI_IDSUBPRODUTO AND
            ES.DTMOVIMENTO <= AI_DTPOSICAO
         ORDER BY DTMOVIMENTO DESC)
   WHERE
      LINHA = 1
   GROUP BY
      IDPRODUTO,
      IDSUBPRODUTO
GO
CREATE OR REPLACE FUNCTION DBA.UF_TPOPERACAO_CMC(
   AI_IDEMPRESA INTEGER,
   AI_IDPLANILHA INTEGER,
   AI_IDPRODUTO INTEGER,
   AI_IDSUBPRODUTO INTEGER,
   AI_NUMSEQUENCIA INTEGER
)
RETURNS TABLE (
    BONIFICA CHAR(1)
)
LANGUAGE SQL
RETURN
    SELECT
        CASE WHEN CF.IDTIPOOPERACAO IN (28,164) THEN 'T' ELSE 'F' END AS BONIFICACAO
    FROM
        DBA.ESTOQUE_ANALITICO EA
    JOIN
        DBA.OPERACAO_INTERNA OP ON
            OP.IDOPERACAO = EA.IDOPERACAO
    JOIN
        DBA.CENARIO_FISCAL CF ON
            CF.IDCENARIOFISCAL = EA.IDCENARIOFISCAL
    JOIN
        DBA.TIPO_OPERACAO TP ON
            TP.IDTIPOOPERACAO = CF.IDTIPOOPERACAO
    WHERE
        EA.IDEMPRESA = AI_IDEMPRESA AND
        EA.IDPLANILHA = AI_IDPLANILHA AND
        EA.IDPRODUTO = AI_IDPRODUTO AND
        EA.IDSUBPRODUTO = AI_IDSUBPRODUTO AND
        EA.NUMSEQUENCIA = AI_NUMSEQUENCIA
GO
CREATE OR REPLACE FUNCTION DBA.UF_CALCULADORA_CMC(
    IN_IDEMPRESA        INTEGER,
    IN_IDPLANILHA       INTEGER,
    IN_IDPRODUTO        INTEGER,
    IN_IDSUBPRODUTO     INTEGER,
    IN_NUMSEQUENCIA     INTEGER,
    IN_DTMOVIMENTO      TIMESTAMP
)
RETURNS TABLE (
    CMCCALCULADO DECIMAL(15,6),
    QTDESTCPCALCULADO DECIMAL(15,6),
    MEMORIA VARCHAR(500)
)
LANGUAGE SQL
READS SQL DATA
DETERMINISTIC
BEGIN ATOMIC

    DECLARE ANT_CUSTOULTCP, ANT_MEDIOCOMPRA, ANTDIA_QUANTIDADEPRO, ATU_CUSTOCOMPRA, ATU_SALDOINIATUAL, ATU_SALDOINIATUAL_HIST, ATU_QTDPRODREGATUAL, LD_CMCCALCULADO, LD_QTDESTCPCALCULADO DECIMAL(15,6);
    DECLARE LS_MEMORIA VARCHAR(500);
    DECLARE LI_DAYS INTEGER DEFAULT 1;
    DECLARE LS_TPMOV VARCHAR(50);

    SET (ANT_CUSTOULTCP, ANT_MEDIOCOMPRA, ANTDIA_QUANTIDADEPRO, ATU_CUSTOCOMPRA, ATU_SALDOINIATUAL, ATU_QTDPRODREGATUAL, LS_TPMOV) =
        (SELECT
            COALESCE(ANTERIOR.CUSTOULTCP, 0)            AS ANTCUSTOULTCP,
            COALESCE(ANTERIOR.MEDIOCOMPRA, 0)           AS ANTMEDIOCOMPRA,
            COALESCE(ANTERIORDIA.QTDPRODANT,0)          AS ANTDIAQUANTIDADEPRO,
            COALESCE(CUSTOESTATUAL.CUSTOCPATUAL,0)      AS ATUCUSTOCOMPRA,
            COALESCE(CUSTOESTATUAL.SALDOINICIALATUAL,0) AS ATUSALDOINICIAL,
            COALESCE(CUSTOESTATUAL.QTDPRODREGATUAL,0)   AS QTDPRODREGATUAL,
            '' AS TPMOV
        FROM
            DBA.DUMMY
        LEFT JOIN (
            SELECT
                COALESCE(TEMP.CUSTOULTIMACOMPRA, 0)     AS CUSTOULTCP,
                COALESCE(TEMP.VALCUSTOMEDIOCOMPRA, 0)   AS MEDIOCOMPRA
            FROM (
                SELECT
                    MC.IDEMPRESA,
                    MC.IDPRODUTO,
                    MC.IDSUBPRODUTO,
                    MC.DTMOVIMENTO,
                    CASE WHEN PC.CUSTOULTIMACOMPRA IS NULL THEN MC.VALCUSTOMEDIOCOMPRA ELSE PC.CUSTOULTIMACOMPRA END AS CUSTOULTIMACOMPRA,
                    MC.VALCUSTOMEDIOCOMPRA,
                    ROW_NUMBER() OVER (
                        PARTITION BY MC.IDEMPRESA, MC.IDPRODUTO, MC.IDSUBPRODUTO
                        ORDER BY MC.DTMOVIMENTO DESC
                    ) AS LINHA
                FROM
                    DBA.MOVIMENTO_CUSTO  MC
                LEFT JOIN
                     DBA.PRODUTO_CUSTO AS PC ON
                        PC.IDEMPRESA = MC.IDEMPRESA AND
                        PC.IDPLANILHA = MC.IDPLANILHA AND
                        PC.IDPRODUTO = MC.IDPRODUTO AND
                        PC.IDSUBPRODUTO = MC.IDSUBPRODUTO AND
                        PC.NUMSEQUENCIA = MC.NUMSEQUENCIA
                WHERE
                    MC.IDEMPRESA        = IN_IDEMPRESA AND
                    MC.IDPRODUTO        = IN_IDPRODUTO AND
                    MC.IDSUBPRODUTO     = IN_IDSUBPRODUTO AND
                    MC.DTMOVIMENTO      < IN_DTMOVIMENTO AND
                    (MC.FLAGALTEROUCUSTO = 'T') AND
                    (MC.ORIGEMMOVIMENTO IN ('NFE', 'PRD', 'TRC') OR
                    (MC.ORIGEMMOVIMENTO IN ('SIP') AND
                    EXISTS (
                        SELECT
                            0
                        FROM
                            DBA.DESAGREGACAO_MOVIMENTO_NOTAS
                        WHERE
                            IDEMPRESA = MC.IDEMPRESA AND
                            IDPLANILHA = MC.IDPLANILHA
                        )
                    ))
            ) TEMP
            WHERE
                LINHA = 1
        ) AS ANTERIOR ON
            0 = 0
        LEFT JOIN (
            SELECT
                COALESCE(SUM(QTDPRODUTO),0) AS QTDPRODANT
            FROM
                DBA.MOVIMENTO_CUSTO MC2
            WHERE
                MC2.IDEMPRESA           = IN_IDEMPRESA AND
                MC2.IDPRODUTO           = IN_IDPRODUTO AND
                MC2.IDSUBPRODUTO        = IN_IDSUBPRODUTO AND
                MC2.DTMOVIMENTO         > DBA.DTINI(IN_DTMOVIMENTO) AND
                MC2.DTMOVIMENTO         < IN_DTMOVIMENTO AND
                (MC2.FLAGALTEROUCUSTO    = 'T') AND
                (MC2.ORIGEMMOVIMENTO     IN ('NFE', 'PRD', 'TRC') OR
                (MC2.ORIGEMMOVIMENTO IN ('SIP') AND
                    EXISTS (
                        SELECT
                            0
                        FROM
                            DBA.DESAGREGACAO_MOVIMENTO_NOTAS
                        WHERE
                            IDEMPRESA = MC2.IDEMPRESA AND
                            IDPLANILHA = MC2.IDPLANILHA
                        )
                    )
                )
        ) ANTERIORDIA (QTDPRODANT) ON
            0 = 0
        LEFT JOIN (
            SELECT
                CASE WHEN TPOPER.BONIFICA = 'T' THEN 0.00 ELSE COALESCE(MC3.CUSTOULTIMACOMPRA,0) END AS CUSTOCPATUAL,
                (COALESCE(SLDEST.QTDATUALESTOQUE,0) - CASE WHEN LI_DAYS = 1 THEN 0 ELSE COALESCE(MCUSTO.QTDPRODUTO,0) END) AS SALDOINICIALATUAL,
                COALESCE(MCUSTO.QTDPRODUTO,0) AS QTDPRODREGATUAL
            FROM
                DBA.MOVIMENTO_CUSTO MCUSTO
            LEFT JOIN DBA.PRODUTO_CUSTO MC3 ON
                    MC3.IDEMPRESA = MCUSTO.IDEMPRESA AND
                    MC3.IDPLANILHA = MCUSTO.IDPLANILHA AND
                    MC3.IDPRODUTO = MCUSTO.IDPRODUTO AND
                    MC3.IDSUBPRODUTO = MCUSTO.IDSUBPRODUTO AND
                    MC3.NUMSEQUENCIA = MCUSTO.NUMSEQUENCIA
            LEFT JOIN TABLE (
                DBA.UF_SALDOEST_DTPOSICAO_CMC(MCUSTO.IDPRODUTO, MCUSTO.IDSUBPRODUTO, DATE(MCUSTO.DTMOVIMENTO) - LI_DAYS DAY, MCUSTO.IDEMPRESA)
            ) SLDEST ON
                    SLDEST.IDPRODUTO    = MCUSTO.IDPRODUTO AND
                    SLDEST.IDSUBPRODUTO = MCUSTO.IDSUBPRODUTO
            LEFT JOIN TABLE (
                DBA.UF_TPOPERACAO_CMC (MCUSTO.IDEMPRESA, MCUSTO.IDPLANILHA, MCUSTO.IDPRODUTO, MCUSTO.IDSUBPRODUTO, MCUSTO.NUMSEQUENCIA)
            ) TPOPER ON 0 = 0
            WHERE
                MCUSTO.IDEMPRESA       = IN_IDEMPRESA AND
                MCUSTO.IDPLANILHA      = IN_IDPLANILHA AND
                MCUSTO.IDPRODUTO       = IN_IDPRODUTO AND
                MCUSTO.IDSUBPRODUTO    = IN_IDSUBPRODUTO AND
                MCUSTO.NUMSEQUENCIA    = IN_NUMSEQUENCIA
        ) CUSTOESTATUAL (CUSTOCPATUAL, SALDOINICIALATUAL, QTDPRODREGATUAL) ON
            0 = 0
    );

        SET LS_MEMORIA =        'ANTCMC: '||CAST(ANT_MEDIOCOMPRA AS VARCHAR(20))||'-'||
                                'SLDINI: '||CAST(ATU_SALDOINIATUAL AS VARCHAR(20))||'-'||
                                'QTDDIA: '||CAST(ANTDIA_QUANTIDADEPRO AS VARCHAR(20))||'-'||
                                'CUSCOM: '||CAST(ATU_CUSTOCOMPRA AS VARCHAR(20))||'-'||
                                'QTDREG: '||CAST(ATU_QTDPRODREGATUAL AS VARCHAR(20))||'-'||
                                LS_TPMOV;

    SET ATU_SALDOINIATUAL_HIST = ATU_SALDOINIATUAL;

    IF ATU_SALDOINIATUAL < 0 THEN
        SET ATU_SALDOINIATUAL = 0;
    END IF;


    IF ANT_MEDIOCOMPRA <= 0 THEN
        IF COALESCE(ATU_SALDOINIATUAL_HIST,0)+COALESCE(ANTDIA_QUANTIDADEPRO,0) > 0 THEN
            SET LD_CMCCALCULADO = (
                (COALESCE(ATU_CUSTOCOMPRA,0)*COALESCE((COALESCE(ATU_SALDOINIATUAL_HIST,0)+COALESCE(ANTDIA_QUANTIDADEPRO,0) ),0))
                + (COALESCE(ATU_CUSTOCOMPRA,0)*COALESCE(ATU_QTDPRODREGATUAL,0))
                ) / (
                COALESCE((COALESCE(ATU_SALDOINIATUAL_HIST,0)+COALESCE(ANTDIA_QUANTIDADEPRO,0)),0)
                + COALESCE(ATU_QTDPRODREGATUAL,0)
                );
        ELSE
            SET LD_CMCCALCULADO = COALESCE(ATU_CUSTOCOMPRA, 0);
        END IF;
    ELSE
        IF COALESCE(ATU_SALDOINIATUAL_HIST,0)+COALESCE(ANTDIA_QUANTIDADEPRO,0) > 0 THEN
            SET LD_CMCCALCULADO = (
                (COALESCE(ANT_MEDIOCOMPRA,0)*COALESCE((COALESCE(ATU_SALDOINIATUAL_HIST,0)+COALESCE(ANTDIA_QUANTIDADEPRO,0)),0))
                + (COALESCE(ATU_CUSTOCOMPRA,0)*COALESCE(ATU_QTDPRODREGATUAL,0))
                ) / (
                COALESCE((
                COALESCE(ATU_SALDOINIATUAL_HIST,0)+COALESCE(ANTDIA_QUANTIDADEPRO,0)),0)
                + COALESCE(ATU_QTDPRODREGATUAL,0)
                );
        ELSE
            SET LD_CMCCALCULADO = COALESCE(ATU_CUSTOCOMPRA, 0);
        END IF;
    END IF;

    SET LS_MEMORIA = LS_MEMORIA || ' - LD_CMCCALCULADO:' || CAST(LD_CMCCALCULADO AS VARCHAR(20));
    SET LD_QTDESTCPCALCULADO = COALESCE(ATU_SALDOINIATUAL_HIST,0)+CASE WHEN LI_DAYS = 0 THEN 0.00 ELSE COALESCE(ANTDIA_QUANTIDADEPRO,0) END;

    RETURN (VALUES(LD_CMCCALCULADO, LD_QTDESTCPCALCULADO, LS_MEMORIA));

END
