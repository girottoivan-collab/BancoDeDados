CREATE OR REPLACE TRIGGER DBA.TR_ATCMC_FILA_AD AFTER DELETE ON DBA.MOVIMENTO_CUSTO
REFERENCING OLD AS O
FOR EACH ROW
BEGIN

    DECLARE LI_SESSIONID INTEGER;

    SET LI_SESSIONID = DBA.UF_GET_ID_CONNECTION();

    IF (SELECT COUNT(0) FROM DBA.CUSTO_MEDIO_COMPRA_CTRLTRIGGER WHERE IDSESSIONPROC = LI_SESSIONID) = 0 THEN
        INSERT INTO DBA.CUSTO_MEDIO_COMPRA_PROCESSAMENTO (IDEMPRESA, IDPLANILHA, IDPRODUTO, IDSUBPRODUTO, NUMSEQUENCIA, DTMOVIMENTO, CMCATUAL, STATUS)
        VALUES (O.IDEMPRESA, O.IDPLANILHA, O.IDPRODUTO, O.IDSUBPRODUTO, O.NUMSEQUENCIA, O.DTMOVIMENTO-2 MINUTES, O.VALCUSTOMEDIOCOMPRA, 'P'/*Pendente*/);
    END IF;

END
GO
CREATE OR REPLACE TRIGGER DBA.TR_ATCMC_FILA_AI AFTER INSERT ON DBA.MOVIMENTO_CUSTO
REFERENCING NEW AS N
FOR EACH ROW
WHEN (DATE(N.DTMOVIMENTO) < CURRENT_DATE )
BEGIN

    DECLARE LI_COUNT INTEGER;
    DECLARE LI_SESSIONID INTEGER;

    SET LI_SESSIONID = DBA.UF_GET_ID_CONNECTION();
    SET LI_COUNT = (SELECT COUNT(0) FROM DBA.MOVIMENTO_CUSTO MC WHERE MC.IDEMPRESA = N.IDEMPRESA AND MC.IDPRODUTO = N.IDPRODUTO AND MC.IDSUBPRODUTO = N.IDSUBPRODUTO AND MC.DTMOVIMENTO > N.DTMOVIMENTO);

    IF LI_COUNT > 0 AND (SELECT COUNT(0) FROM DBA.CUSTO_MEDIO_COMPRA_CTRLTRIGGER WHERE IDSESSIONPROC = LI_SESSIONID) = 0 THEN
        INSERT INTO DBA.CUSTO_MEDIO_COMPRA_PROCESSAMENTO (IDEMPRESA, IDPLANILHA, IDPRODUTO, IDSUBPRODUTO, NUMSEQUENCIA, DTMOVIMENTO, CMCATUAL, STATUS)
        VALUES (N.IDEMPRESA, N.IDPLANILHA, N.IDPRODUTO, N.IDSUBPRODUTO, N.NUMSEQUENCIA, N.DTMOVIMENTO-2 MINUTES, N.VALCUSTOMEDIOCOMPRA, 'P'/*Pendente*/);
    END IF;

END
GO
CREATE OR REPLACE TRIGGER DBA.TR_ATCMC_FILA_AU AFTER UPDATE ON DBA.MOVIMENTO_CUSTO
REFERENCING NEW AS N OLD AS O
FOR EACH ROW
BEGIN

    DECLARE LI_COUNT INTEGER;
    DECLARE LD_DTCTRL TIMESTAMP;
    DECLARE LI_SESSIONID INTEGER;

    SET LI_SESSIONID = DBA.UF_GET_ID_CONNECTION();

    IF N.DTMOVIMENTO > O.DTMOVIMENTO THEN
        SET LD_DTCTRL = O.DTMOVIMENTO-2 MINUTES;
    ELSE
        SET LD_DTCTRL = N.DTMOVIMENTO-2 MINUTES;
    END IF;

    SET LI_COUNT = (SELECT COUNT(0) FROM DBA.MOVIMENTO_CUSTO MC WHERE MC.IDEMPRESA = N.IDEMPRESA AND MC.IDPRODUTO = N.IDPRODUTO AND MC.IDSUBPRODUTO = N.IDSUBPRODUTO AND MC.DTMOVIMENTO > N.DTMOVIMENTO);

    IF LI_COUNT > 0 AND (SELECT COUNT(0) FROM DBA.CUSTO_MEDIO_COMPRA_CTRLTRIGGER WHERE IDSESSIONPROC = LI_SESSIONID) = 0 THEN
        INSERT INTO DBA.CUSTO_MEDIO_COMPRA_PROCESSAMENTO (IDEMPRESA, IDPLANILHA, IDPRODUTO, IDSUBPRODUTO, NUMSEQUENCIA, DTMOVIMENTO, CMCATUAL, STATUS)
        VALUES (N.IDEMPRESA, N.IDPLANILHA, N.IDPRODUTO, N.IDSUBPRODUTO, N.NUMSEQUENCIA, LD_DTCTRL, N.VALCUSTOMEDIOCOMPRA, 'P'/*Pendente*/);
    END IF;

END